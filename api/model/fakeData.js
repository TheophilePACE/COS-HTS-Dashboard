formatConsumption = data =>
    data.datasets
        .map(ds => ds.data.map((d, i) =>
            ({
                time: data.labels[i],
                quantity: d,
                applianceId: ds.label
            })
        ))
        .reduce((pv, cv) => pv.concat(cv), [])

const formatPrice = data =>
    data.datasets
        .map(ds => ds.data.map((d, i) =>
            ({
                time: data.labels[i],
                price: d,
                retailerId: ds.label
            })
        ))
        .reduce((pv, cv) => pv.concat(cv), [])

